export function directoryForAudience(audience: Audience) {
  return supportDirectory.filter((entry) =>
    audience === 'users' ? entry.visibleToUsers : entry.visibleToCounselors,
  );
}

export function publicEmergencyContacts() {
  return directoryForAudience('users')
    .filter((entry) => entry.phoneNumbers.length > 0)
    .map((entry) => ({
      id: entry.id,
      name: entry.organizationName,
      phoneNumber: entry.phoneNumbers.join(' / '),
      category: entry.serviceCategory,
      description: `${entry.referralNotes} Availability: ${entry.availability}`,
      country: 'ZW' as const,
    }));
}
